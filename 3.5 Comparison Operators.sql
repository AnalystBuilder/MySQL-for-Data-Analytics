# Comparison Operators

# =, <>, >,<


SELECT *
FROM bakery.customer_orders
WHERE tip = 1
;


SELECT *
FROM bakery.customer_orders
WHERE tip != 1
;


SELECT *
FROM bakery.customer_orders
WHERE tip > 5
;


SELECT *
FROM bakery.customer_orders
WHERE tip >= 5
;



SELECT *
FROM bakery.customer_orders
WHERE tip <= 5
;






































